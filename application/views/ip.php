                        <h2>What is IP?</h2>
                        <p>IP stands for intellectual property and relates to the category of the assets that are
                            created by human intellect. In a
                            broader sense, intellectual property is a set of intangible assets owned by a company and
                            legally protected from the use
                            or implementation without consent of the owner/rightsholder by the third parties.</p>
                        <p>As we said an intangible asset is a non-physical asset that a company owns. In most
                            legislations in the world,
                            intangible assets have the same level of protection rights that apply to physical property
                            that are called tangible
                            assets.</p>
                        <p>Below we will briefly describe the common types of Intellectual Property:</p>
                        <h6>Trademarks</h6>
                        <p>A trademark is a word, phrase, symbol, and/or design that identifies and distinguishes the
                            product of one company from
                            the others. A trademark is often associated with a company's brand. The main goal of the
                            trademark is to differentiate a
                            product from similar other products (services) and enable its owner (owner / rightsholder)
                            to gain additional financial
                            benefits from its commercial use in return for a clear identity and greater faith in the
                            brand’s function
                            (representation). A good example of a registered trademark can be the logo and brand name of
                            "Coca Cola” that is owned
                            by the Coca-Cola Company.</p>
                        <h6>Patents</h6>
                        <p>A patent is a property right granted by a governmental body (usually called patent agency).
                            The patent allows the
                            inventor to have the exclusive rights to the invention, which could be in the form of
                            industrial design, physical
                            invention, useful model.</p>
                        <h6>Copyrights</h6>
                        <p>Copyright is a type of intellectual property that provides authors and creators of original
                            material the exclusive right
                            to use, copy, or duplicate their material and additionally protects it from unauthorized
                            use. A copyright owner/
                            rightsholder can assign his legal rights for authorized use by virtue of a licensing
                            agreement to use the work. The
                            typical works that can be protected as copyrights are a literary work, song, movie or
                            software. However, in order to be
                            protected as a copyright, the object must meet certain requirements:</p>
                        <ol>
                            <li>To be original, in other words, it cannot be copied from something else.</li>
                            <li>To be creative means that a work needs to demonstrate a very amount of creativity.</li>
                            <li>To be in a fixed form. To meet the fixation requirement, a work must be fixed in a
                                tangible medium of expression. From a legal point of view, a work is considered to be
                                fixed as long it’s permanent or stable to be perceived, reproduced, or otherwise
                                communicated for a period of more than transitory duration.</li>
                        </ol>
                        <h6>Trade Secrets</h6>
                        <p>A trade secret means information which:</p>
                        <ul>
                            <li>is secret in the sense that it is not generally known among, or readily accessible to,
                                persons within the circles that normally deal with the kind of information in question;
                            </li>
                            <li>has commercial value because it is secret;</li>
                            <li>has been subject to reasonable steps under the circumstances, by the person lawfully in
                                control of the information (Company), to keep it secret.</li>
                        </ul>
                        <p>Trade secrets must be actively protected by the company and are typically the result of a
                            company's research and
                            development and usually evolves into creation of a specific confidential regime established
                            within the company. Examples
                            of trade secrets could be a design, pattern, recipe, formula, or proprietary process. A good
                            example of a
                            well-implemented and protected trade-secret is a secret formula of Coca-Cola beverage.</p>
